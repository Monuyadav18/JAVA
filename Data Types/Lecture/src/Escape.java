public class Escape {
    public static void main(String[] args) {
        System.out.println("Hello Coders!");
        System.out.println("Hello \tCoders!");
        System.out.println("Hello \bCoders!");
        System.out.println("Hello \nCoders!");
        System.out.println("Hello 'Coders!'");
        System.out.println("Hello \"Coders!\"");
        System.out.println("Hello \\Coders!\\");

    }
}
